function profile = stage_profiles(profile_name)
% stage_profiles
% Stage template profiles for smoke/formal and ablation scenarios.

    switch lower(string(profile_name))
        case "benchmark_smoke"
            profile = struct('runs', 1, 'func_ids', struct('cec2017', 1:3, 'cec2022', 1:3));
        case "benchmark_formal"
            profile = struct('runs', 5, 'func_ids', struct('cec2017', 1:30, 'cec2022', 1:12));
        case "v3_reduced_ablation"
            profile = struct('suite', 'cec2017', 'reduced_func_ids', [1, 2, 3, 12, 13, 14, 15, 18, 19], 'smoke_runs', 2, 'formal_runs', 5);
        case "v3_dual_ablation"
            profile = struct('suites', {'cec2017'}, 'smoke_runs', 1, 'formal_runs', 5, ...
                'smoke_func_ids', struct('cec2017', 1:3), ...
                'formal_func_ids', struct('cec2017', 1:30));
        case "route_a_budget_adaptive_abc_smoke"
            profile = struct( ...
                'suite', 'cec2022', ...
                'func_ids', [6, 7, 8, 10, 11, 12], ...
                'smoke_runs', 10, ...
                'dim', 10, ...
                'pop_size', 30, ...
                'maxFEs', 30000, ...
                'long_maxFEs', 300000, ...
                'rng_seed', 20260320);
        otherwise
            error('Unsupported stage profile: %s', profile_name);
    end
end
