function item = make_item(paper_name, internal_id, entry_func, aliases, tier)
    normalized_aliases = unique(cellstr(upper(string([aliases, internal_id, entry_func]))), 'stable');

    item = struct();
    item.paper_name = char(string(paper_name));
    item.internal_id = char(lower(string(internal_id)));
    item.entry_func = char(entry_func);
    item.aliases = normalized_aliases;
    item.tier = char(tier);
end
