function save_protocol_snapshot(result_dir, snapshot)
% save_protocol_snapshot
% Persist protocol snapshot as MAT and CSV with backward-compatible keys.

    if nargin < 2
        error('save_protocol_snapshot requires result_dir and snapshot');
    end

    save(fullfile(result_dir, 'protocol_snapshot.mat'), 'snapshot');

    names = { ...
        'mode', ...
        'timestamp', ...
        'suites', ...
        'dim', ...
        'pop_size', ...
        'maxFEs', ...
        'runs', ...
        'rng_seed', ...
        'seed_list_defined', ...
        'seed_list_count', ...
        'experiment_name', ...
        'result_root', ...
        'save_curve', ...
        'save_mat', ...
        'save_csv', ...
        'objective_wrapper_note', ...
        'objective_wrapper_hook_defined'};

    values = { ...
        string(snapshot.mode), ...
        string(snapshot.timestamp), ...
        string(strjoin(snapshot.run_cfg.suites, ';')), ...
        string(snapshot.run_cfg.dim), ...
        string(snapshot.run_cfg.pop_size), ...
        string(snapshot.run_cfg.maxFEs), ...
        string(snapshot.run_cfg.runs), ...
        string(snapshot.run_cfg.rng_seed), ...
        string(isfield(snapshot.run_cfg, 'seed_list') && ~isempty(snapshot.run_cfg.seed_list)), ...
        string(local_seed_list_count(snapshot.run_cfg)), ...
        string(snapshot.run_cfg.experiment_name), ...
        string(snapshot.run_cfg.result_root), ...
        string(snapshot.run_cfg.save_curve), ...
        string(snapshot.run_cfg.save_mat), ...
        string(snapshot.run_cfg.save_csv), ...
        string(snapshot.objective_wrapper_note), ...
        string(snapshot.objective_wrapper_hook_defined)};

    T = table(string(names(:)), string(values(:)), 'VariableNames', {'key','value'});
    writetable(T, fullfile(result_dir, 'protocol_snapshot.csv'));
end

function n = local_seed_list_count(run_cfg)
    n = 0;
    if isfield(run_cfg, 'seed_list') && ~isempty(run_cfg.seed_list)
        n = numel(run_cfg.seed_list);
    end
end
