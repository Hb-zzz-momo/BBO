function aggregate_table = metrics_build_aggregate_table(summary_table)
% metrics_build_aggregate_table
% Aggregate per-function summary to algorithm-level statistics.

    algs = unique(string(summary_table.algorithm_name), 'stable');
    rows = table();

    for i = 1:numel(algs)
        alg = algs(i);
        subset = summary_table(string(summary_table.algorithm_name) == alg, :);
        row = table( ...
            alg, ...
            height(subset), ...
            mean(subset.mean), ...
            mean(subset.std), ...
            mean(subset.best), ...
            mean(subset.worst), ...
            mean(subset.median), ...
            mean(subset.avg_runtime), ...
            mean(subset.avg_used_FEs), ...
            'VariableNames', {'algorithm_name','func_count','mean_of_mean','mean_of_std','mean_of_best','mean_of_worst','mean_of_median','mean_runtime','mean_used_FEs'});
        rows = [rows; row]; %#ok<AGROW>
    end

    aggregate_table = sortrows(rows, {'mean_of_mean','mean_runtime'}, {'ascend','ascend'});
end
